//

#include <iostream>

using namespace std;


int main()
{
    setlocale(0, "rus");
    int x = 10;     // определяем переменную
    int* p;         // определяем указатель
    p = &x;         // указатель получает адрес переменной

    cout << "адрес переменной = " << p << endl;
    cout << "значение переменной = " << *p << endl;
    return 0;
}
